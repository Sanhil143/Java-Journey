class Datatypes{
    public static void main(String[] args){
        // in java there are 8 types of primitive datatypes
        byte a = 123;       // it contains 8bits
        short b = 12345;    // 16bits
        int c = 1234567890; // 32bits
        long d = 1234567890; // 64bits
        float aa = 12.0f;  // 32bits
        double bb = 45.256; // 64bits
        char name = 'a'; //16bits
        boolean isTrue = true; // 1bits
        // non-primitive datatypes
        // arrays
        // class that is object in java
        // string
        // interfaces and many more
    }
}