class Variables{
    public static void main(String[] args){
        int a;  //declaration
        a = 50; //initialization
        System.out.println(a);
        //or
        int b = 27; //declaration & initialization
        System.out.println(b);
        // values of variables can changes anytime

        int c = 99;
        System.out.println(c);
        c = 199;
        System.out.println(c);
    }
}